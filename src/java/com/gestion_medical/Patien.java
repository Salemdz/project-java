package com.gestion_medical;

/**
 *
 * @author Panika
 */
public class Patien {
    private int id;
    private User user;

    public Patien() {
    }

    public Patien(int id, User user) {
        this.id = id;
        this.user = user;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }
}
